namespace TestingCarculator
{
    public class Calculator
    {
        public int GetSum(int a, int b)
        {
            return a + b;
        }

        public int GetSubstract(int a, int b)
        {
            return a - b;
        }

        public int GetMultiplication(int a, int b)
        {
            return a * b;
        }

        public int GetDivision(int a, int b)
        {
            return a / b;
        }
    }
}
